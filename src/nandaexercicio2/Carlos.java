package nandaexercicio2;

public class Carlos {

	

	public static void somatorio (double numero1, double numero2) {
		double resultadoSoma = numero1 + numero2;
		System.out.println(resultadoSoma);
	}
	
	public static void subtracao (double numero1, double numero2) {
		double resultadoSubtracao = numero1 - numero2;
		System.out.println(resultadoSubtracao);
	}
	
	public static void multiplicacao (double numero1, double numero2) {
		double resultadomutiplicacao = numero1 * numero2;
		System.out.println(resultadomutiplicacao);
	}
	
	public static void divisao (double numero1, double numero2) {
		double resultadodivisao = numero1 / numero2;
		System.out.println(resultadodivisao);
	}
}
